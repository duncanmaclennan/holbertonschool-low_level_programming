#include "3-calc.h"
/**
 * op_add - adds
 * @a: the first
 * @b: the second
 * Return: the result
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts
 * @a: the first
 * @b: the second
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies
 * @a: the first
 * @b: the second
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides
 * @a: the first
 * @b: the second
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns remainder
 * @a: the first
 * @b: the second
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
